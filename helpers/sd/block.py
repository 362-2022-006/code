def makeValue(byteSeq):
    ret = 0
    for b in byteSeq[::-1]:
        ret = ret * 256 + b
    return ret


def printPartitionEntry(n):
    entry = getRegion(0x1AE + 16 * n, 16)
    if entry[0] == 0: print("Valid")
    elif entry[0] == 0x80: print("Bootable")
    else: print("Invalid")

    print(f"Partition type: 0x{entry[4]:02x}")

    print(entry[1:4])
    print(entry[4:7])

    start = makeValue(entry[8:12])
    size = makeValue(entry[12:16])

    print(f"Start: {start:08x} = {start}")
    print(f"Length: {size:08x} = {size} ({size/2048/1024:.03f} GiB)")

